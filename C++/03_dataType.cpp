#include <iostream>
#include <string>
using namespace std;

int main() {
    // Integer
    int myNum = 5;
    cout << "Integer: " << myNum << endl;

    //LOng
    long x = 2300000000000;
    cout << "Long no. is " << x <<"\n";
    // Floating point
    float myFloatNum = 5.99f;
    cout << "Float: " << myFloatNum << endl;

    // Double
    double myDoubleNum = 9.98;
    cout << "Double: " << myDoubleNum << endl;

    // Character
    char myLetter = 'D';
    cout << "Character: " << myLetter << endl;

    // Boolean
    bool myBoolean = true;
    cout << "Boolean: " << myBoolean << endl;

    // String
    string myText = "Hello";
    cout << "String: " << myText << endl;

    // Wide Character

    return 0;
}
